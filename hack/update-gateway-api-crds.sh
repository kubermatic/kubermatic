set -euo pipefail

cd $(dirname $0)/..
source hack/lib.sh

containerize ./hack/update-gateway-api-crds.sh

gatewayVersion="1.2.1"

# List of CRDs to download and process with their actual filenames
declare -A CRDS=(
    ["gateways"]="gateway.networking.k8s.io_gateways.yaml"
    ["grpcroutes"]="gateway.networking.k8s.io_grpcroutes.yaml"
    ["httproutes"]="gateway.networking.k8s.io_httproutes.yaml"
    ["tcproutes"]="gateway.networking.k8s.io_tcproutes.yaml"
    ["udproutes"]="gateway.networking.k8s.io_udproutes.yaml"
    ["tlsroutes"]="gateway.networking.k8s.io_tlsroutes.yaml"
    ["referencegrants"]="gateway.networking.k8s.io_referencegrants.yaml"
)

tmpFile=gateway-api.tar.gz
tmpDir=$(mktemp -d)

curl -fLo "$tmpFile" "https://github.com/kubernetes-sigs/gateway-api/archive/refs/tags/v$gatewayVersion.tar.gz"
tar xzf "$tmpFile" -C "$tmpDir"
rm "$tmpFile"

crdDir=pkg/crd/k8s.io/
rm -f $crdDir/gateway.networking.k8s.io_*.yaml

# Copy only the specific CRDs we want
for crd in "${!CRDS[@]}"; do
    cp "$tmpDir/gateway-api-$gatewayVersion/config/crd/experimental/${CRDS[$crd]}" "$crdDir/"
done

# Clean up temp directory
rm -rf "$tmpDir"

# Process CRDs in a subshell to contain directory changes
(
    cd "$crdDir"
    for filename in gateway.networking.k8s.io_*.yaml; do
        tmpFile="$filename.bak"

        mv "$filename" "$tmpFile"
        echo -e "# This file has been generated by hack/update-gateway-api-crds.sh, DO NOT EDIT.\n" > "$filename"
        # use yq instead of cat to normalize indentations
        yq '.' "$tmpFile" >> "$filename"
        rm "$tmpFile"
    done
)

# Run codegen to add annotations
./hack/update-codegen.sh