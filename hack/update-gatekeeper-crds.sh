set -euo pipefail

cd $(dirname $0)/..
source hack/lib.sh

gatekeeperVersion="v3.12.0"

tmpFile=gatekeeper.tar.gz
curl -fLo "$tmpFile" "https://github.com/open-policy-agent/gatekeeper/archive/refs/tags/$gatekeeperVersion.tar.gz"

crdDir=pkg/controller/user-cluster-controller-manager/resources/resources/gatekeeper/static/
rm -f $crdDir/*

tar_flags="xzf"
if tar --help 2>&1 | grep -q -- '--wildcards'; then
  tar_flags="xzf --wildcards"
fi

tar $tar_flags "$tmpFile" -C "$crdDir" --strip-components=5 '*/charts/gatekeeper/crds/*'
rm "$tmpFile"

cd "$crdDir"

for filename in *.yaml; do
  tmpFile="$filename.bak"

  mv "$filename" "$tmpFile"
  echo -e "# This file has been generated by hack/update-gatekeeper-crds.sh, DO NOT EDIT.\n" > "$filename"
  # use yq instead of cat to normalize indentations
  yq '.' "$tmpFile" >> "$filename"
  rm "$tmpFile"
done
